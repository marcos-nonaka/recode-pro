public class Permissoes {
	
	private int id_permissao;
	private String tipo;
	
	public int getId_permissao() {
		return id_permissao;
	}
	public void setId_permissao(int id_permissao) {
		this.id_permissao = id_permissao;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
}